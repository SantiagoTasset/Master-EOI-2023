public class SistemasNumericos {
    public static void main(String[] args) {
         int numero;

         //Decimal
        numero = 14;
        System.out.println("numero = " + numero);

        numero = 0b00001110;
        System.out.println("numero = " + numero);

        numero = 016;
        System.out.println("numero = " + numero);

        numero = 0x0e;
        System.out.println("numero = " + numero);
    }
}
