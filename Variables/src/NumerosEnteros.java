public class NumerosEnteros {
    public static void main(String[] args) {
        // Tipo byte
        byte numeroByte = 99;
        System.out.println(" Tipo Byte ");
        System.out.println("numeroByte = " + numeroByte);
        System.out.println("Ocupa " + Byte.BYTES + " bytes ");
        System.out.println("Ocupa " + Byte.SIZE + " bytes ");
        System.out.println("Maximo " + Byte.MAX_VALUE + " bytes ");
        System.out.println("Minimo " + Byte.MIN_VALUE + " bytes ");
        System.out.println(" -------------- ");

        // Tipo short

        short numeroShort = 25400;

        System.out.println(" Tipo Short ");
        System.out.println("numeroShort = " + numeroShort);
        System.out.println("Ocupa " + Short.BYTES + " bytes ");
        System.out.println("Ocupa " + Short.SIZE + " bytes ");
        System.out.println("Maximo " + Short.MAX_VALUE + " bytes ");
        System.out.println("Minimo " + Short.MIN_VALUE + " bytes ");
        System.out.println(" -------------- ");

        // Tipo int

        int numeroInt = 589000;

        System.out.println(" Tipo Int ");
        System.out.println("numeroInt = " + numeroInt);
        System.out.println("Ocupa " + Integer.BYTES + " bytes ");
        System.out.println("Ocupa " + Integer.SIZE + " bytes ");
        System.out.println("Maximo " + Integer.MAX_VALUE + " bytes ");
        System.out.println("Minimo " + Integer.MIN_VALUE + " bytes ");
        System.out.println(" -------------- ");

        //Tipo long

        long numeroLong = 45568000000l;

        System.out.println(" Tipo Int ");
        System.out.println("numeroLong = " + numeroLong);
        System.out.println("Ocupa " + Long.BYTES + " bytes ");
        System.out.println("Ocupa " + Long.SIZE + " bytes ");
        System.out.println("Maximo " + Long.MAX_VALUE + " bytes ");
        System.out.println("Minimo " + Long.MIN_VALUE + " bytes ");
        System.out.println(" -------------- ");

    }
}
